    private void txtNombreDeEvaluacionKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreDeEvaluacionKeyReleased
        String entrada = txtNombreDeEvaluacion.getText().trim();
    
    if (!entrada.isEmpty()) {
        String[] partes = entrada.split("\\s+");
        StringBuilder siglas = new StringBuilder();

        // Tomar primeras letras de palabras que no sean números
        for (String parte : partes) {
            if (!parte.matches("\\d+")) {
                siglas.append(parte.charAt(0));
            }
        }

        // Buscar si hay un número en la entrada
        String numero = "";
        for (String parte : partes) {
            if (parte.matches("\\d+")) {
                numero = parte;
                break;
            }
        }

        txtSiglas.setText(siglas.toString().toUpperCase() + numero);
    } else {
        txtSiglas.setText("");
    }

    }//GEN-LAST:event_txtNombreDeEvaluacionKeyReleased

