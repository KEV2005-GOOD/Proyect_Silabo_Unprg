    private boolean validardatos() {
        String mensaje="";
        JComponent componente = null;
        String valor;
        
        valor=this.txtNombreDeEvaluacion.getText();
        if (valor.isEmpty()|| valor.isBlank()) {
            mensaje+="Debe Llenar el campo de Nombre de la evaluacion";
            componente=this.txtNombreDeEvaluacion;
        }
        valor=this.txtCronograma.getText() ;
        if (valor.isEmpty()|| valor.isBlank()) {
            mensaje+="\n Debe llenar el campo de Cronograma";
        }
        
        if (componente!=null) {
            JOptionPane.showMessageDialog(this, mensaje);
            return false;
        }
        return true;
    }
    
    


    private EvaluacionesCalificadas crearEvaluacion() {
        EvaluacionesCalificadas eval= new EvaluacionesCalificadas();
        
        eval.setNombreEvaluacion(this.txtNombreDeEvaluacion.getText());
        eval.setSiglasEvaluacion(this.txtSiglas.getText());
        eval.setPeso((double) this.spPeso.getValue());
        eval.setCronograma(this.txtCronograma.getText());
        
        return eval;
    }

   public  EvaluacionesCalificadas agregar() {
       return this.evaluacion;
    }
    
    public void cargarDatos(EvaluacionesCalificadas evaluacion) {
    this.txtNombreDeEvaluacion.setText(evaluacion.getNombreEvaluacion());
    this.txtSiglas.setText(evaluacion.getSiglasEvaluacion());
    this.spPeso.setValue(evaluacion.getPeso());
    this.txtCronograma.setText(evaluacion.getCronograma());
    this.setVisible(true);
}
